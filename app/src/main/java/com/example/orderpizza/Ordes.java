package com.example.orderpizza;

public class Ordes {
    double pizza_precio;
    double pepperoni_price=0;
    double jamon_price=0;
    double carne_price=0;
    double bacon_price=0;
    double loroco_price=0;
    double pina_price=0;
    double pimientos_price=0;

    public Ordes() {
    }

    public double getPizza_precio() {
        return pizza_precio;
    }

    public void setPizza_precio(double pizza_precio) {
        this.pizza_precio = pizza_precio;
    }

    public double getPepperoni_price() {
        return pepperoni_price;
    }

    public void setPepperoni_price(double pepperoni_price) {
        this.pepperoni_price = pepperoni_price;
    }

    public double getJamon_price() {
        return jamon_price;
    }

    public void setJamon_price(double jamon_price) {
        this.jamon_price = jamon_price;
    }

    public double getCarne_price() {
        return carne_price;
    }

    public void setCarne_price(double carne_price) {
        this.carne_price = carne_price;
    }

    public double getBacon_price() {
        return bacon_price;
    }

    public void setBacon_price(double bacon_price) {
        this.bacon_price = bacon_price;
    }

    public double getLoroco_price() {
        return loroco_price;
    }

    public void setLoroco_price(double loroco_price) {
        this.loroco_price = loroco_price;
    }

    public double getPina_price() {
        return pina_price;
    }

    public void setPina_price(double pina_price) {
        this.pina_price = pina_price;
    }

    public double getPimientos_price() {
        return pimientos_price;
    }

    public void setPimientos_price(double pimientos_price) {
        this.pimientos_price = pimientos_price;
    }
}
